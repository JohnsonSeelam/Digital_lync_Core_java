package com.dl.casting;

public class Eg1 {

	public static void main(String[] args) {
		//Implicit casting
		byte b=10;
		System.out.println(b);
		short s=b;
		System.out.println(s);
		int i=s;
		System.out.println(i);
		long l=i;
		System.out.println(l);
		float f=l;
		System.out.println(f);
		double d=f;
		System.out.println(d);
		char ch='A';
		int i1=ch;
		System.out.println(i1);

	}

}
